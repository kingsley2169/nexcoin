export type TransactionType =
	| "deposit"
	| "fee"
	| "investment"
	| "profit"
	| "referral"
	| "withdrawal";

export type TransactionStatus =
	| "accruing"
	| "completed"
	| "credited"
	| "failed"
	| "pending"
	| "processing";

export type TransactionDirection = "in" | "out";

export type Transaction = {
	amount: number;
	amountUsd: number;
	assetSymbol: string;
	createdAt: string;
	detail: string;
	direction: TransactionDirection;
	fee?: number;
	feeAsset?: string;
	fullAddress?: string;
	id: string;
	notes?: string;
	planName?: string;
	reference: string;
	status: TransactionStatus;
	txHash?: string;
	type: TransactionType;
};

